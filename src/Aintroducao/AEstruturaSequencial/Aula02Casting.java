package Aintroducao.AEstruturaSequencial;

public class Aula02Casting {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        double resultado = (double) a / b;
        System.out.println(resultado);

        int c;
        double d = 5.0;
        c = (int) d;

        System.out.println(c);

    }
}
